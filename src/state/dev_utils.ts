export const convertAlpha: (num: number) => string = (num: number) => {
  switch (num) {
    case 10:
      return "A";
    case 11:
      return "B";
    case 12:
      return "C";
    case 13:
      return "D";
    case 14:
      return "E";
    case 15:
      return "F";
    default:
      return num.toString();
  }
};

export const convertRGBtoHex: (rgb: string | null) => string = (
  rgb: string | null
) => {
  if (rgb != null) {
    let res = rgb
      .split(";")
      .map((x) => {
        let A = Math.floor(parseInt(x) / 16);
        let B = parseInt(x) % 16;
        return `${convertAlpha(A)}${convertAlpha(B)}`;
      })
      .join("");
    return res;
  }

  return "000000";
};

export const useWhiteForeground: (rgb: string | undefined) => boolean = (
  rgb: string | undefined
) => {
  if (rgb == null) return false;
  const rgbList = rgb.split(";").map((x) => (x != "0" ? parseInt(x) : 0));

  let val = Math.sqrt(
    Math.pow(rgbList[0], 2) * 0.299 +
      Math.pow(rgbList[1], 2) * 0.587 +
      Math.pow(rgbList[2], 2) * 0.114
  );

  return val <= 130 ? true : false;
};

export const convertTimeFromInt: (x: string) => string = (x: string) => {
  let hh = parseInt(x);

  return hh == 0
    ? "12am"
    : hh <= 11
    ? `${hh}am`
    : hh == 12
    ? "12pm"
    : hh == 24
    ? "All Day"
    : `${hh - 12}pm`;
};

export const EmailValidator: (email: string) => boolean = (email: string) => {
  const pattern = RegExp(
    /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
  );

  return pattern.test(email);
};

export const PasswordValidator: (pwd: string) => boolean = (pwd: string) => {
  const pattern = RegExp(
    /^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$&*-]).{6,}$/
  );
  return pattern.test(pwd);
};

export const DisplayNameValidator: (dispName: string) => boolean = (
  dispName: string
) => {
  const pattern = RegExp(/^(?!.*\.\.)(?!.*\.$)[^\W][\w.]{2,29}$/);

  return pattern.test(dispName);
};
